package Home_work_2.Additional_tasks.new_5;

public class Cat {
    public String name;
    public int age;
    public int weight;

    public Cat(String name, int age, int weight) {
        this.name = name;
        this.age = age;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
    public int getWeight() {
        return weight;
    }

}